'use strict';
var CollectionTest = TestCase('collection');
CollectionTest.prototype.testMaxSubSum = function(){
	var scenarios = [
        [[-1, -2, -3], -1, [0, 0]],
        [[0, -1, -2, -3], 0, [0, 0]],
        [[-1, -2, 5, 0, -3, 7], 9, [2, 5]],
        [[-1, -2, 5, 0, -3, 7, -1], 9, [2, 5]],
        [[-1, -2, 5, 0, -3, 7, 0], 9, [2, 5]],
        [[0, -1, -2, 5, 0, -3, 7, 0], 9, [3, 6]],
        [[5, 0, -1, -2, 5, 0, -3, 7, 0], 11, [0, 7]],
        [[5, 0, -1, -2, 5, 0, -3, 7, 0, -11], 11, [0, 7]],
        [[-11, 5, 0, -1, -2, 5, 0, -3, 7, 0], 11, [1, 8]],
        [[5, 0, -2, -3, 5, 0, -3, 7, 0], 9, [4, 7]],
        [[5, 0, -1, -2, -3, 5, 0, -3, 7, 0], 9, [5, 8]],
        [[5, 0, -1, -2, -3, 0, 5, 0, -3, 7, 0], 9, [6, 9]],
        [[5, 0, -1, -2, -3, 0, -1, 5, 0, -3, 7, 0], 9, [7, 10]],
        [[0, 5, 0, -1, -2, -3, 0, -1, 5, 0, -3, 7, 0], 9, [8, 11]],
        [[-5, 5, 0, -1, -2, -3, 0, -1, 5, 0, -3, 7, 0], 9, [8, 11]],
        [[5, 0, -1, 6, -2, -3, 0, -6, 5, 0, -3, 7, 0], 10, [0, 3]],
        [[5, -1, 6, -2, -3, 0, -6, 5, 0, -3, 7, 0], 10, [0, 2]],
        [[5, -1, 6, -2, -3, 0, -6, 5, 0, -3, 8, 0], 10, [0, 2]],
        [[5, -1, 6, -2, -3, 0, -6, 5, 0, -3, 9, 0], 11, [7, 10]],
        [[1, 2, 3, 4, 5, 0, -1, -2, -3], 15, [0, 4]],
        [[0, -1, -2, -3, 1, 2, 3, 4, 5], 15, [4, 8]]
    ];
	scenarios.forEach(function(scenario){
		var arr = scenario[0];
		assertEquals("Max sum of " + arr, scenario[1], arr.maxSubSum());
		assertEquals("Range of max sum" + arr, scenario[2], arr.maxSubSum(true));
	});
};